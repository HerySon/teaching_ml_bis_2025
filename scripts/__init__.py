def main(description: str, parser: argparse.ArgumentParser) -> argparse.Namespace:
    print(description, " - Version:", __version__)
    parser.add_argument("--file_path", type=str, required=True,
                         help="Chemin vers le fichier CSV")
    parser.add_argument("--output_path", type=str, default="cleaned_data.csv",
                         help="Chemin du fichier de sortie")
    parser.add_argument("--missing_threshold", type=float, default=0.5,
                         help="Seuil de valeurs manquantes pour supprimer une colonne")
    parser.add_argument("--pattern_col", type=str, default="serving_size",
                         help="Colonne pour extraire un motif particulier")
    parser.add_argument("--pattern", type=str, default="(\\d+)",
                         help="Motif regex à extraire")
    parser.add_argument("--irrelevant_cols", type=str, nargs='+',
                         default=["id", "unwanted_col"], help="Colonnes non pertinentes")
    parser.add_argument("--category_threshold", type=int, default=10,
                         help="Nombre max de catégories pour les variables ordinales")
    parser.add_argument("--limit", type=int, default=None,
                         help="Nombre maximum de lignes à charger")
    parser.add_argument("--sample_size", type=float, default=0.1,
                         help="Proportion de l'échantillon à sélectionner")
    parser.add_argument("--random_state", type=int, default=42,
                         help="Valeur pour initialiser la graine aléatoire")
    parser.add_argument("--plot_type", type=str, default="numeric",
                         help="Type de graphique à générer")
    parser.add_argument("--colunm_name", type=str, default="cities",
                         help="Nom de la colonne à visualiser")
    parser.add_argument('--min_freq', type=float, default=0.01,
                         help="Fréquence minimale pour la suppression des catégories rares")
    parser.add_argument('--n_components', type=int, default=8,
                         help="Nombre de composants pour HashingEncoder")
    parser.add_argument('--n_clusters', type=int, default=5,
                         help="Nombre de clusters pour la fusion des catégories via K-means")
    return parser.parse_args()

def get_all_columns(df) -> list:
    return df.columns.tolist()

